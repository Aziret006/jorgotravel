class Command(BaseCommand):
    help = 'Наполняет базу турами и направлениями с сайта JorgoTravel'

    @transaction.atomic
    def handle(self, *args, **options):
        for order, data in enumerate(TOURS):
            itinerary = data.pop('itinerary')
            tour, created = Tour.objects.update_or_create(
                slug=data['slug'],
                defaults={**data, 'sort_order': order},
            )
            tour.itinerary.all().delete()
            TourDay.objects.bulk_create(
                TourDay(tour=tour, day=day, title=title, description=description)
                for day, title, description in itinerary
            )
            action = 'создан' if created else 'обновлён'
            self.stdout.write(f'Тур «{tour.title}» {action}')

        for order, data in enumerate(DESTINATIONS):
            tour_slug = data.pop('tour_slug')
            tour = Tour.objects.filter(slug=tour_slug).first() if tour_slug else None
            destination, created = Destination.objects.update_or_create(
                key=data['key'],
                defaults={**data, 'tour': tour, 'sort_order': order},
            )
            action = 'создано' if created else 'обновлено'
            self.stdout.write(f'Направление «{destination.name}» {action}')

        for order, data in enumerate(REVIEWS):
            tour_slug = data.pop('tour_slug')
            tour = Tour.objects.filter(slug=tour_slug).first() if tour_slug else None
            review, created = Review.objects.update_or_create(
                name=data['name'],
                date_label=data['date_label'],
                defaults={**data, 'tour': tour, 'sort_order': order},
            )
            action = 'создан' if created else 'обновлён'
            self.stdout.write(f'Отзыв «{review.name}» {action}')

        self.stdout.write(self.style.SUCCESS(
            f'Готово: {Tour.objects.count()} туров, {Destination.objects.count()} направлений, '
            f'{Review.objects.count()} отзывов.'
        ))
